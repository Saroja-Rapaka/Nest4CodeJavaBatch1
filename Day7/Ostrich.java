package oops2;

public class Ostrich 
{
	void birdSound()
	{
		System.out.println("Ostrich makes hissing sound");
	}
	void birdColor()
	{
	System.out.println("Ostrich's color is black and white");
	}
	public static void main(String args[])
	{
	  Ostrich o=new Ostrich();
	 
	  o.birdSound();
	  o.birdColor();
	 
	}

}
